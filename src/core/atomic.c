#include <stdint.h>


// ---------------------------------------------------------------------------
int32_t atomic_xchg_i32 (int32_t* ref, int32_t val)
{
  int32_t ex = *ref;
  *ref = val;
  return ex;
}


// ---------------------------------------------------------------------------
int32_t atomic_inc_i32 (int32_t* ref)
{
  return ++(*ref);
}


// ---------------------------------------------------------------------------
int32_t atomic_dec_i32 (int32_t* ref)
{
  return --(*ref);
}


// ---------------------------------------------------------------------------
int32_t atomic_add_i32 (int32_t* ref, int32_t val)
{
  return (*ref) += val;
}

